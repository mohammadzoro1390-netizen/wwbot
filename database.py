import sqlite3
from config import DB_PATH

def get_conn():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn

def setup_db():
    conn = get_conn()
    c = conn.cursor()

    # ادمین‌ها
    c.execute('''CREATE TABLE IF NOT EXISTS admins (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id TEXT UNIQUE NOT NULL,
        name TEXT,
        is_owner INTEGER DEFAULT 0
    )''')

    # کشورها
    c.execute('''CREATE TABLE IF NOT EXISTS countries (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT UNIQUE NOT NULL,
        flag TEXT DEFAULT "🏳",
        continent TEXT DEFAULT "",
        money INTEGER DEFAULT 5000000000,
        oil INTEGER DEFAULT 1000,
        satisfaction INTEGER DEFAULT 100,
        daily_oil INTEGER DEFAULT 0,
        max_players INTEGER DEFAULT 10,
        current_players INTEGER DEFAULT 0,
        is_active INTEGER DEFAULT 1,
        is_vip INTEGER DEFAULT 0,
        is_sanctioned INTEGER DEFAULT 0,
        sanction_days INTEGER DEFAULT 0
    )''')

    # بازیکنان
    c.execute('''CREATE TABLE IF NOT EXISTS players (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id TEXT UNIQUE NOT NULL,
        username TEXT DEFAULT "",
        country_id INTEGER DEFAULT NULL,
        last_daily TEXT DEFAULT NULL
    )''')

    # انواع تجهیزات نظامی
    c.execute('''CREATE TABLE IF NOT EXISTS equipment_types (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        emoji TEXT DEFAULT "⚔️",
        category TEXT DEFAULT "army",
        continent TEXT DEFAULT "همه",
        price INTEGER DEFAULT 1000,
        max_buy INTEGER DEFAULT 999,
        description TEXT DEFAULT "",
        extra TEXT DEFAULT ""
    )''')

    # تجهیزات کشورها
    c.execute('''CREATE TABLE IF NOT EXISTS country_equipment (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL,
        equipment_type_id INTEGER NOT NULL,
        amount INTEGER DEFAULT 0,
        UNIQUE(country_id, equipment_type_id)
    )''')

    # ساختمان‌ها / پروژه‌های اقتصادی
    c.execute('''CREATE TABLE IF NOT EXISTS building_types (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT UNIQUE NOT NULL,
        emoji TEXT DEFAULT "🏛",
        continent TEXT DEFAULT "همه",
        price INTEGER DEFAULT 50000000,
        effect_type TEXT DEFAULT "money",
        effect_value INTEGER DEFAULT 0,
        max_buy INTEGER DEFAULT 1,
        description TEXT DEFAULT ""
    )''')

    # ساختمان‌های کشورها
    c.execute('''CREATE TABLE IF NOT EXISTS buildings (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL,
        building_type_id INTEGER NOT NULL,
        amount INTEGER DEFAULT 0,
        UNIQUE(country_id, building_type_id)
    )''')

    # نیروی انسانی
    c.execute('''CREATE TABLE IF NOT EXISTS troop_types (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        emoji TEXT DEFAULT "🪖",
        continent TEXT DEFAULT "همه",
        price INTEGER DEFAULT 500000
    )''')

    # نیروی انسانی کشورها
    c.execute('''CREATE TABLE IF NOT EXISTS country_troops (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL,
        troop_type_id INTEGER NOT NULL,
        amount INTEGER DEFAULT 0,
        UNIQUE(country_id, troop_type_id)
    )''')

    # نفتکش‌ها
    c.execute('''CREATE TABLE IF NOT EXISTS tanker_types (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        emoji TEXT DEFAULT "🚢",
        continent TEXT DEFAULT "همه",
        price INTEGER DEFAULT 250000000,
        capacity INTEGER DEFAULT 400
    )''')

    c.execute('''CREATE TABLE IF NOT EXISTS country_tankers (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL,
        tanker_type_id INTEGER NOT NULL,
        amount INTEGER DEFAULT 0,
        UNIQUE(country_id, tanker_type_id)
    )''')

    # تنظیمات daily
    c.execute('''CREATE TABLE IF NOT EXISTS daily_config (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        money_reward INTEGER DEFAULT 500000000,
        oil_reward INTEGER DEFAULT 200,
        satisfaction_reward INTEGER DEFAULT 5
    )''')

    # بکاپ
    c.execute('''CREATE TABLE IF NOT EXISTS backups (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        backup_time TEXT DEFAULT CURRENT_TIMESTAMP,
        created_by TEXT,
        data TEXT NOT NULL
    )''')

    # لاگ
    c.execute('''CREATE TABLE IF NOT EXISTS logs (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        action TEXT,
        performed_by TEXT,
        details TEXT,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )''')

    # خریدهای پولی (اختراع، VIP، ماهواره و ...)
    c.execute('''CREATE TABLE IF NOT EXISTS premium_orders (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id TEXT NOT NULL,
        country_name TEXT DEFAULT "",
        order_type TEXT NOT NULL,
        item_name TEXT NOT NULL,
        price_toman INTEGER NOT NULL,
        status TEXT DEFAULT "pending",
        confirm_code TEXT DEFAULT NULL,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP,
        confirmed_at TEXT DEFAULT NULL
    )''')

    # VIP کشورها
    c.execute('''CREATE TABLE IF NOT EXISTS country_vip (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL UNIQUE,
        vip_type TEXT DEFAULT "",
        expire_date TEXT DEFAULT NULL,
        is_active INTEGER DEFAULT 1
    )''')

    # ماهواره‌های جاسوسی
    c.execute('''CREATE TABLE IF NOT EXISTS satellites (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL UNIQUE,
        level INTEGER DEFAULT 1,
        is_active INTEGER DEFAULT 1
    )''')

    # اختراعات
    c.execute('''CREATE TABLE IF NOT EXISTS inventions (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        country_id INTEGER NOT NULL,
        invention_type TEXT NOT NULL,
        name TEXT DEFAULT "",
        description TEXT DEFAULT "",
        is_active INTEGER DEFAULT 1,
        daily_production INTEGER DEFAULT 0,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )''')

    # گروهک‌های تروریستی / هکری
    c.execute('''CREATE TABLE IF NOT EXISTS groups (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_id TEXT UNIQUE NOT NULL,
        group_name TEXT NOT NULL,
        group_type TEXT DEFAULT "terror",
        budget INTEGER DEFAULT 3000000000,
        daily_income INTEGER DEFAULT 100000000,
        troops INTEGER DEFAULT 500,
        loyalty INTEGER DEFAULT 100,
        op_power INTEGER DEFAULT 100,
        security INTEGER DEFAULT 100,
        host_country TEXT DEFAULT "",
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )''')

    # اتحادها
    c.execute('''CREATE TABLE IF NOT EXISTS alliances (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT UNIQUE NOT NULL,
        leader_country_id INTEGER,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )''')

    c.execute('''CREATE TABLE IF NOT EXISTS alliance_members (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        alliance_id INTEGER NOT NULL,
        country_id INTEGER NOT NULL,
        UNIQUE(country_id)
    )''')

    # تنگه‌ها
    c.execute('''CREATE TABLE IF NOT EXISTS straits (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT UNIQUE NOT NULL,
        controller_country TEXT DEFAULT "",
        is_closed INTEGER DEFAULT 0,
        closed_by TEXT DEFAULT "",
        closed_at TEXT DEFAULT NULL
    )''')

    # وضعیت جنگ
    c.execute('''CREATE TABLE IF NOT EXISTS wars (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        attacker_country_id INTEGER NOT NULL,
        defender_country_id INTEGER NOT NULL,
        status TEXT DEFAULT "active",
        started_at TEXT DEFAULT CURRENT_TIMESTAMP,
        ended_at TEXT DEFAULT NULL,
        winner_id INTEGER DEFAULT NULL
    )''')


    # تحریم‌ها
    c.execute('''CREATE TABLE IF NOT EXISTS sanctions (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        target_country_id INTEGER NOT NULL,
        sanctioner_country_id INTEGER NOT NULL,
        start_date TEXT DEFAULT CURRENT_TIMESTAMP,
        end_date TEXT
    )''')

    # اتحادها
    c.execute('''CREATE TABLE IF NOT EXISTS alliances (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT UNIQUE NOT NULL,
        leader_country_id INTEGER,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )''')

    c.execute('''CREATE TABLE IF NOT EXISTS alliance_members (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        alliance_id INTEGER NOT NULL,
        country_id INTEGER NOT NULL,
        UNIQUE(country_id)
    )''')

    # پیش‌فرض daily
    if conn.execute("SELECT COUNT(*) FROM daily_config").fetchone()[0] == 0:
        conn.execute("INSERT INTO daily_config (money_reward,oil_reward,satisfaction_reward) VALUES (500000000,200,5)")

    conn.commit()
    conn.close()
    print("✅ دیتابیس آماده شد.")


